public class CompoundExpressions {


    public static void main(String[] args){

        int age = 65;
        System.out.println("Demonstrating compound expressions : ");
        System.out.println("Demo Addition Operation : " + (age + 55));
        System.out.println("Demo Postincrement Operation : " + age++);
        System.out.println("Demo Post Decrement Operation : " + age--);
        System.out.println("Demo String Concatenation Operation : " + "A" + "B");
        System.out.println("Demo String Concatenation Operation 2 : " + "A" + 5);
        Short x = 32767;
        System.out.println("Demo Short Operation : " + ++x);


    }


}
